class Student
{
    string firstname;
    string lastName;
    int birthYear;
    string classe;
    public string Firstname
    {
        get { return firstname; }
        set { firstname = value; }
    }
    public string LastName
    {
        get { return lastName; }
        set { lastName = value; }
    }
    public int BirthYear
    {
        get { return birthYear; }
        set { birthYear = value; }
    }
    public string Classe
    {
        get { return classe; }
        set { classe = value; }
    }
}